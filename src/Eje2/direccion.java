package Eje2;

public class direccion {

	String calle;
	String ciudad;
	String codigo_postal;
	String pais;
	
	public direccion(String calle, String ciudad, String codigo_Postal, String pais) {
		
		this.calle=calle;
		this.ciudad=ciudad;
		this.codigo_postal=codigo_Postal;
		this.pais=pais;
	}
	
}
